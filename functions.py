# напиши функцию, которая возвращает тип данных на русском языке:
# число, строка, булевый, None, список, кортеж, множество, словарь
# пример: get_type("что-то") возвращает "Это строка."
# пример2: get_type(42) возвращает "Это словарь."
def get_type(arg):
    translation_into_Russian = {
    "int": "целое число", 
    "float": "вещественное число", 
    "str": "строка", 
    "bool": "логический тип", 
    "NoneType": "значит, что значение отсутствует",
    "list": "список", 
    "tuple": "кортеж", 
    "set": "множество", 
    "dict" : "словарь"
    }
    print("Это " + translation_into_Russian[type(arg).__name__])

get_type(True)

