# напиши функцию, которая принимает на вход любое
# количество чисел и сообщает, есть ли среди них чётное
def even_function(*any_number):
    for number in any_number: 
        if number % 2 == 0: 
            print('Есть четные числа')
            break
        else:
            print('Нет четных чисел')
even_function(1,2,3)

